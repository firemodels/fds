   READ(LU,'(A)',END=10) TEXT
   TLOOP: DO II=1,255-LEN_TRIM(NAME)
      IF (TEXT(II:II+LEN_TRIM(NAME)-1)==TRIM(NAME)) THEN
         FOUND = .TRUE.
         EXIT READLOOP
      ENDIF
   ENDDO TLOOP
ENDDO READLOOP
 
10 REWIND(LU)
RETURN

END SUBROUTINE SEARCH_INPUT_FILE




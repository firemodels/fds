SUBROUTINE CHECKREAD(NAME,LU,IOS)
 
INTEGER :: II
INTEGER, INTENT(OUT) :: IOS
INTEGER, INTENT(IN) :: LU
CHARACTER(4), INTENT(IN) :: NAME
CHARACTER(80) TEXT
IOS = 1

READLOOP: DO
   READ(LU,'(A)',END=10) TEXT
   TLOOP: DO II=1,72
      IF (TEXT(II:II)/='&' .AND. TEXT(II:II)/=' ') EXIT TLOOP
      IF (TEXT(II:II)=='&') THEN
         IF (TEXT(II+1:II+4)==NAME) THEN
            BACKSPACE(LU)
            IOS = 0
            EXIT READLOOP
         ELSE
            CYCLE READLOOP
         ENDIF
      ENDIF
   ENDDO TLOOP
ENDDO READLOOP
 
10 RETURN
END SUBROUTINE CHECKREAD



SUBROUTINE SEARCH_INPUT_FILE(NAME,LU,FOUND)
 
INTEGER :: II
LOGICAL, INTENT(OUT) :: FOUND
INTEGER, INTENT(IN) :: LU
CHARACTER(*), INTENT(IN) :: NAME
CHARACTER(255) TEXT

FOUND = .FALSE.
REWIND(LU)


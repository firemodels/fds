SUBROUTINE CORRECT_PRESSURE

USE MATH_FUNCTIONS, ONLY : GAUSSJ
REAL(EB) :: A_LOC(NCGC,NCGC),A(NCGC,NCGC),B_LOC(NCGC),B(NCGC)
INTEGER :: IERROR,NM

A     = 0._EB
B     = 0._EB
A_LOC = 0._EB
B_LOC = 0._EB

MESH_LOOP_2: DO NM=1,NMESHES
   IF (PROCESS(NM)/=MYID) CYCLE MESH_LOOP_2
   CALL COMPUTE_A_B(A_LOC,B_LOC,NM)

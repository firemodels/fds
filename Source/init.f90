PRIMARY_THIN_WALL_LOOP_1: DO ITW=1,M%N_THIN_WALL_CELLS
   TW => M%THIN_WALL(ITW)
   SF => SURFACE(TW%SURF_INDEX)
   IF (.NOT.SF%HT3D .OR. TW%BACK_INDEX<=0) CYCLE PRIMARY_THIN_WALL_LOOP_1
   M2  => MESHES(TW%BACK_MESH)   ! M2 can be M
   TWB => M2%THIN_WALL(TW%BACK_INDEX)
   BC  => M%BOUNDARY_COORD(TW%BC_INDEX)
   ONE_D => M%BOUNDARY_ONE_D(TW%OD_INDEX)
   OB => M%OBSTRUCTION(TW%OBST_INDEX)
   CALL ADJUST_HT3D_ONE_D
ENDDO PRIMARY_THIN_WALL_LOOP_1

CONTAINS

SUBROUTINE ADJUST_HT3D_ONE_D

IF (ONE_D%LAYER_THICKNESS(1)<TWO_EPSILON_EB) THEN
   SELECT CASE(ABS(BC%IOR))
      CASE(1) ; ONE_D%LAYER_THICKNESS(1) = OB%X2 - OB%X1
      CASE(2) ; ONE_D%LAYER_THICKNESS(1) = OB%Y2 - OB%Y1
      CASE(3) ; ONE_D%LAYER_THICKNESS(1) = OB%Z2 - OB%Z1 
   END SELECT
ENDIF

! Get the new N_CELLS_MAX for this wall cell

CALL GET_N_LAYER_CELLS(SF%MIN_DIFFUSIVITY(1),SF%SWELL_RATIO*ONE_D%LAYER_THICKNESS(1),SF%STRETCH_FACTOR(1), &
                       SF%CELL_SIZE_FACTOR,SF%CELL_SIZE,SF%N_LAYER_CELLS_MAX(1),ONE_D%N_CELLS_MAX,&
                       ONE_D%SMALLEST_CELL_SIZE(1),ONE_D%DDSUM(1))

! Get the new N_CELLS_INI for this wall cell

CALL GET_N_LAYER_CELLS(SF%MIN_DIFFUSIVITY(1),ONE_D%LAYER_THICKNESS(1),SF%STRETCH_FACTOR(1), &
                       SF%CELL_SIZE_FACTOR,SF%CELL_SIZE,SF%N_LAYER_CELLS_MAX(1),ONE_D%N_LAYER_CELLS(1),&
                       ONE_D%SMALLEST_CELL_SIZE(1),ONE_D%DDSUM(1))

ONE_D%N_CELLS_INI = ONE_D%N_LAYER_CELLS(1)

IF (ALLOCATED(ONE_D%X)) DEALLOCATE(ONE_D%X)             ; ALLOCATE(ONE_D%X(0:ONE_D%N_CELLS_MAX))
IF (ALLOCATED(ONE_D%TMP)) DEALLOCATE(ONE_D%TMP)         ; ALLOCATE(ONE_D%TMP(0:ONE_D%N_CELLS_MAX+1))
IF (ALLOCATED(ONE_D%RHO_C_S)) DEALLOCATE(ONE_D%RHO_C_S) ; ALLOCATE(ONE_D%RHO_C_S(ONE_D%N_CELLS_MAX))
IF (ALLOCATED(ONE_D%K_S)) DEALLOCATE(ONE_D%K_S)         ; ALLOCATE(ONE_D%K_S(0:ONE_D%N_CELLS_MAX+1))
DO NN=1,SF%N_MATL
   IF (ALLOCATED(ONE_D%MATL_COMP(NN)%RHO_DOT)) DEALLOCATE(ONE_D%MATL_COMP(NN)%RHO_DOT)
   ALLOCATE(ONE_D%MATL_COMP(NN)%RHO_DOT(0:ONE_D%N_CELLS_MAX+1))
   IF (ALLOCATED(ONE_D%MATL_COMP(NN)%RHO)) DEALLOCATE(ONE_D%MATL_COMP(NN)%RHO)
   ALLOCATE(ONE_D%MATL_COMP(NN)%RHO(0:ONE_D%N_CELLS_MAX+1))
ENDDO
DO NN=1,SF%N_SPEC
   IF (ALLOCATED(ONE_D%SPEC_COMP(NN)%RHO_ZZ)) DEALLOCATE(ONE_D%SPEC_COMP(NN)%RHO_ZZ)
   ALLOCATE(ONE_D%SPEC_COMP(NN)%RHO_ZZ(0:ONE_D%N_CELLS_MAX+1))
ENDDO

! Get the new cell coordinates

ALLOCATE(X_S_OLD(0:1)); X_S_OLD=0._EB
ALLOCATE(N_LAYER_CELLS_OLD(SF%N_LAYERS)) ; N_LAYER_CELLS_OLD=1
ALLOCATE(REMESH_LAYER(SF%N_LAYERS)) ; REMESH_LAYER=.TRUE.
CALL GET_WALL_NODE_COORDINATES(ONE_D%N_LAYER_CELLS(1),1,SF%N_LAYERS,ONE_D%N_LAYER_CELLS, &
                               N_LAYER_CELLS_OLD,ONE_D%SMALLEST_CELL_SIZE, &
                               SF%STRETCH_FACTOR,REMESH_LAYER,ONE_D%X,X_S_OLD)
DEALLOCATE(X_S_OLD)
DEALLOCATE(N_LAYER_CELLS_OLD)
DEALLOCATE(REMESH_LAYER)

! Reset initial values for some reallocated arrays

ONE_D%TMP = SF%TMP_INNER(1)
ONE_D%RHO_C_S = 1.E6_EB
ONE_D%K_S = 0._EB
DO NN=1,SF%N_MATL
   ONE_D%MATL_COMP(NN)%RHO_DOT = 0._EB
   ONE_D%MATL_COMP(NN)%RHO(0:ONE_D%N_CELLS_INI+1) = SF%RHO_0(1,NN)
ENDDO
DO NN=1,SF%N_SPEC
   ONE_D%SPEC_COMP(NN)%RHO_ZZ(0:ONE_D%N_CELLS_INI+1) = SF%PHIRHOZ_0(1,NN)
ENDDO

END SUBROUTINE ADJUST_HT3D_ONE_D


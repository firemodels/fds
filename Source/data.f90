PR_GAS = PR_T
FORMULAIN = FORMULA
FORMULA = 'null'

LISTED  = .TRUE.
ATOM_COUNTS = 0._EB

IF (GAS_INDEX==-1) THEN
   ! NITROGEN VALUES
   SIGMA = 3.798_EB
   EPSOK = 71.4_EB
   H_F = -1.E30_EB
   MW = 28.01340_EB
   PR_GAS = 0.71_EB ! JPCRD 19(5)
   LISTED = .FALSE.
ELSE
   TD => THERMO_DATA(GAS_INDEX)
   SIGMA   = TD%SIGMA
   EPSOK   = TD%EPSOK
   FORMULA = TD%FORMULA
   H_F     = TD%H_F
   PR_GAS  = TD%PR
   IF (RADCAL_NAME=='null') RADCAL_NAME = TD%RADCAL_ID

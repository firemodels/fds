      CASE(-2) 
         M2%H(II,JJ-1,KK)   = M2%REAL_RECV_PKG7(LL+3)
         M2%FVY(II,JJ-1,KK) = M2%REAL_RECV_PKG7(LL+5)
      CASE( 2) 
         M2%H(II,JJ+1,KK)   = M2%REAL_RECV_PKG7(LL+3)

      ELSE
         CP = 102.2585_EB - 1084.688_EB/TE + 0.04410701_EB*TE - 0.00001181179_EB*TE**2 + 0.000000001601012_EB*TE**3 &
              - 8.408214E-14_EB*TE**4 - 13.55214_EB*LOG(TE) !kJ/mol/K

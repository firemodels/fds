         CP = 1033.429_EB + TE * 0.090798_EB !J/kg/K
      ELSE
         CP = 43.9033_EB - 69.46386_EB/TE + 0.01806059_EB*TE - 0.000006173999_EB*TE**2 + 0.000000001003309_EB*TE**3 &
              - 6.3073984E-14_EB*TE**4 - 3.423053*LOG(TE) !kJ/mol/K

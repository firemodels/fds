   M%RHO(:,:,K) = M%RHO_0(K)
   M%RHOS(:,:,K)= M%RHO_0(K)
   M%TMP(:,:,K) = M%TMP_0(K)
   M%U(:,:,K)   = M%U_WIND(K)
   M%V(:,:,K)   = M%V_WIND(K)
   M%W(:,:,K)   = M%W_WIND(K)
ENDDO

M%US    = M%U
M%VS    = M%V
M%WS    = M%W
M%FVX   = 0._EB
M%FVY   = 0._EB
M%FVZ   = 0._EB
M%KRES  = 0._EB
IF (INITIAL_SPEED>0._EB) THEN
   M%H  = 0._EB
   M%HS = 0._EB
ELSE
   M%H  = 0.5_EB*(U0**2+V0**2+W0**2)
   M%HS = 0.5_EB*(U0**2+V0**2+W0**2)
ENDIF
M%DDDT  = 0._EB
M%D     = 0._EB
M%DS    = 0._EB
M%Q     = 0._EB

! Calculate LES filter width

ALLOCATE(M%LES_FILTER_WIDTH(0:IBP1,0:JBP1,0:KBP1),STAT=IZERO) ; CALL ChkMemErr('INIT','LES_FILTER_WIDTH',IZERO)

DO K=0,KBP1
   DO J=0,JBP1
      DO I=0,IBP1
         M%LES_FILTER_WIDTH(I,J,K) = LES_FILTER_WIDTH_FUNCTION(M%DX(I),M%DY(J),M%DZ(K))
      ENDDO
   ENDDO
ENDDO

! Viscosity

ZZ_GET(1:N_TRACKED_SPECIES) = SPECIES_MIXTURE(1:N_TRACKED_SPECIES)%ZZ0
CALL GET_VISCOSITY(ZZ_GET,MU_N,TMPA)
M%MU = MU_N

CS = C_SMAGORINSKY
SELECT CASE(TURB_MODEL)
   CASE(CONSMAG,DYNSMAG)
      DO K=0,KBP1
         DO J=0,JBP1
            DO I=0,IBP1
               DELTA = M%LES_FILTER_WIDTH(I,J,K)
               M%CSD2(I,J,K) = (CS*DELTA)**2
            ENDDO
         ENDDO
      ENDDO
END SELECT

! Initialize mass fraction arrays

DO N=1,N_TRACKED_SPECIES
   M%ZZ(:,:,:,N)  = SPECIES_MIXTURE(N)%ZZ0
   M%ZZS(:,:,:,N) = SPECIES_MIXTURE(N)%ZZ0
ENDDO
DO N=N_TRACKED_SPECIES+1,N_TOTAL_SCALARS
   M%ZZ(:,:,:,N)  = INITIAL_UNMIXED_FRACTION
   M%ZZS(:,:,:,N) = INITIAL_UNMIXED_FRACTION
ENDDO

! Over-ride default ambient gas species mass fractions if defined in INIT

M%IWORK1=0
DO N=1,N_INIT
   IN => INITIALIZATION(N)
   IF ((IN%NODE_ID/='null')) CYCLE
   IF (.NOT. (IN%ADJUST_SPECIES_CONCENTRATION)) CYCLE
   DO K=0,KBP1
      DO J=0,JBP1
         DO I=0,IBP1
            IF (M%XC(I) > IN%X1 .AND. M%XC(I) < IN%X2 .AND. &
                M%YC(J) > IN%Y1 .AND. M%YC(J) < IN%Y2 .AND. &
                M%ZC(K) > IN%Z1 .AND. M%ZC(K) < IN%Z2) THEN
               IF (M%IWORK1(I,J,K)==0) THEN
                  M%IWORK1(I,J,K) = N
                  M%ZZ(I,J,K,1:N_TRACKED_SPECIES) = IN%MASS_FRACTION(1:N_TRACKED_SPECIES)
                  M%ZZS(I,J,K,1:N_TRACKED_SPECIES) = IN%MASS_FRACTION(1:N_TRACKED_SPECIES)
               ENDIF
            ENDIF
         ENDDO
      ENDDO
   ENDDO
ENDDO


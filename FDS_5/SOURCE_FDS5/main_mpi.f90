REAL(EB) :: T_SUM, TNOW
 
TNOW = SECOND()
NM = MYID+1

T_SUM = 0.
SUM_LOOP: DO I=2,N_TIMERS
   IF (I==9 .OR. I==10) CYCLE SUM_LOOP
   T_SUM = T_SUM + TUSED(I,NM)
ENDDO SUM_LOOP
 
NECYC          = MAX(1,NTCYC(NM)-NCYC(NM))
T_PER_STEP(NM) = (T_SUM-T_ACCUM(NM))/REAL(NECYC,EB)
